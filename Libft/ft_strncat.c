#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *s1p;

	s1p = s1;
	while (*s1p != '\0')
		s1p++;
	while (n-- > 0 && *s2 != '\0')
		*s1p++ = *s2++;
	*s1p = '\0';
	return (s1);
}
